      <h4 class="media-heading">Rich Chigga</h4>
      Brian Imanuel (lahir di Jakarta, Indonesia, 2 September 1999; umur 18 tahun), yang lebih dikenal dengan nama panggung Rich Brian (sebelumnya Rich Chigga dan Brian), adalah seorang penyanyi rap, produser rekaman, dan penulis lagu dari Indonesia. Ia dikenal melalui singelnya yang bertajuk "Dat $tick", yang dirilis pada bulan Maret 2016.


Kehidupan pribadi
Brian Imanuel lahir pada tanggal 2 September 1999 di Jakarta, Indonesia. Dia adalah keturunan Tionghoa. Dibesarkan di Indonesia, Brian memulai kariernya melalui media sosial pada tahun 2010 saat masih berusia 11 tahun. Pada awalnya, dia sering mengirimkan video komedi gelap yang kerap berbau budaya Amerika, yang seringkali ditontonnya melalui video dari internet. Dia belajar bahasa Inggris secara otodidak dengan menonton video YouTube dan mendengarkan lagu-lagu dari para penyanyi rap seperti Childish Gambino, 2 Chainz, Macklemore, dan Tyler, The Creator.

Karier
2015–2016: Awal karier
Brian pertama kali merilis singelnya, berjudul "Living the Dream" pada tanggal 17 Juli 2015 di akun YouTube miliknya. Lagu tersebut diproduksi oleh DJ Smokey. Brian kemudian merilis singel debutnya "Dat $tick" pada tanggal 22 Februari 2016.Lagu ini mencuri perhatian di panggung internasional setelah sebuah video reaksi diunggah, yang menampilkan para penyanyi rap Amerika Serikat seperti Ghostface Killah, 21 Savage, Tory Lanez, MadeinTYO, Desiigner dan lainnya."Dat Stick" menduduki peringkat empat di tangga lagu Bubbling Under R&B/Hip-Hop Singles.
