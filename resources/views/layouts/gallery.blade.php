      <h4 class="media-heading">Judika</h4>
      Judika Nalon Abadi Sihotang yang lebih dikenal dengan Judika atau Judika Sihotang (lahir di Dairi, Sidikalang, Sumatra Utara, Indonesia, 31 Agustus 1978; umur 39 tahun) adalah seorang aktor, penyanyi dan model berkebangsaan Indonesia yang meraih runner-up Indonesian Idol ke-2. Ia adalah anak ke-6 dari 7 bersaudara yang menghabiskan masa kecilnya dengan berbagai macam kegiatan.

Karier
Bakat bernyanyinya sudah tampak semenjak Judika masih duduk di bangku Sekolah Dasar Inpres di Brastagi. Bahkan ia sempat membuat grup vokal bersama Roy (adik), Juneidi (sahabat) dan Jonathan (sahabat) dan juga dikontrak di sebuah hotel Internasional berbintang 5 di Brastagi. Judika kecil tidak puas berbangga hati ketika banyak yang bilang ia punya potensi menjadi bintang. Lantas ia menjajal kemampuannya lewat Bahana Suara Pelajar dan keluar sebagai juara pertama untuk tingkat Sumatera Utara dan cukup puas di posisi ketiga untuk tingkat Nasional. Selain bersolo karier, saat ini Judika juga menjajal sebagai anak band dengan menjadi vokalis Mahadewa sebuah band yang digawangi Ahmad Dhani.

Tak hanya berprofesi sebagai penyanyi, Judika juga mulai merambah dunia akting. Ia bermain di film Si Jago Merah pada 2008, dan pada 2009 bermain di film The Tarix Jabrix 2. Bersama kekasihnya, Duma Riris Silalahi ia juga bermain di film Impal (pariban).


